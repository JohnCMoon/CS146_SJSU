public class BinaryNode
{
	private int data;
	private int height;
	private BinaryNode left;
	private BinaryNode right;

	public BinaryNode getLeft() {
		return left;
	}

	public BinaryNode getRight() {
		return right;
	}
	
	public void setLeft(BinaryNode node) {
		left = node;
	}	

	public void setRight(BinaryNode node) {
		right = node;
	}


}
